#[derive(Debug, Clone, PartialEq)]
pub enum TokenKind {
    // Keywords
    On,
    End,
    If,
    Then,
    Else,
    Repeat,
    With,
    To,
    Put,
    Into,
    Get,
    Set,
    The,
    Of,
    Go,
    Next,
    Prev,
    First,
    Last,
    Card,
    Field,
    Button,
    Show,
    Hide,
    Answer,
    Ask,
    Play,
    Sound,
    Wait,
    Second,
    Seconds,
    Pass,
    Send,
    Global,
    Return,
    Not,
    And,
    Or,
    Is,
    True,
    False,
    Fetch,
    Sort,
    By,
    Each,
    Lines,
    Words,
    Items,
    Characters,
    Line,
    Word,
    Item,
    Character,
    While,
    Do,
    Add,
    Subtract,
    Multiply,
    Divide,
    Mod,
    Contains,
    There,
    In,
    After,
    Before,
    It,
    Me,
    Empty,
    Space,
    Tab,
    Newline,
    Quote,
    Colon,
    Number,
    // "number" keyword used in "the number of ..."
    Delete,
    Select,
    Visual,
    Effect,
    Lock,
    Unlock,
    Screen,
    Message,
    Box,
    Background,
    Stack,
    Down,
    Up,

    // Literals
    Identifier,
    StringLiteral,
    NumberLiteral,

    // Operators
    Plus,
    Minus,
    Star,
    Slash,
    Ampersand,
    DoubleAmpersand,
    Equals,
    NotEquals,
    LessThan,
    GreaterThan,
    LessOrEqual,
    GreaterOrEqual,
    LeftParen,
    RightParen,
    Comma,
    Caret,

    // Structure
    NewlineTok,
    Eof,
}

#[derive(Debug, Clone)]
pub struct Token {
    pub kind: TokenKind,
    pub text: String,
    pub line: usize,
    pub col: usize,
}

impl Token {
    pub fn new(kind: TokenKind, text: impl Into<String>, line: usize, col: usize) -> Self {
        Self {
            kind,
            text: text.into(),
            line,
            col,
        }
    }
}
